def load_dataset_index(dataset_dir: Path) -> dict | None:
    """Load the dataset index.json."""
    index_path = dataset_dir / "index.json"
    if index_path.exists():
        with open(index_path) as f:
            return json.load(f)
    return None


def generate_caption(metadata: dict | None, category: str | None = None) -> str:
    """
    Generate a descriptive caption for a garment image.
    Used for text-conditional LoRA training.
    """
    if not metadata:
        return f"An Indian {category.lower() if category else 'ethnic wear garment'}"

    parts = [f"An Indian"]

    # Category
    cat = category or metadata.get("category", "garment")
    parts.append(cat)

    # Fabric
    fabric = metadata.get("fabric_estimate")
    if fabric:
        fabric_label = FABRIC_LABELS.get(fabric, fabric)
        parts.append(f"made of {fabric_label}")

    # Pattern
    pattern = metadata.get("pattern")
    if pattern and pattern.lower() != "plain":
        parts.append(f"with {pattern.lower()} pattern")

    # Embellishments
    embellishments = metadata.get("embellishments", [])
    if embellishments and embellishments[0] not in (None, "None"):
        parts.append(f"featuring {embellishments[0]} work")

    # Colors
    primary_color = metadata.get("primary_color")
    if primary_color:
        parts.append(f"in {primary_color} color")

    # Occasion
    occasions = metadata.get("occasions", [])
    if occasions:
        occ = occasions[0]
        occ_label = OCCASION_LABELS.get(occ, f"suitable for {occ}")
        parts.append(f"designed {occ_label}")

    return ", ".join(parts) + "."


def copy_directory(src: Path, dst: Path, desc: str = "Copying"):
    """Copy files from src to dst with progress."""
    dst.mkdir(parents=True, exist_ok=True)
    files = list(src.iterdir()) if src.exists() else []
    for f in tqdm(files, desc=desc):
        if f.is_file():
            shutil.copy2(f, dst / f.name)


def prepare_catvton_dataset(
    dataset_dir: Path,
    output_dir: Path,
    categories: list[str] | None = None,
):
    """
    Prepare dataset in CatVTON-compatible format.
    
    Structure:
        output/
        ├── cloth/                  # Garment images
        ├── agnostic-mask/          # Binary masks
        ├── image/                  # (empty — needs person images for supervised training)
        ├── image-densepose/        # (empty — needs DensePose)
        └── metadata.json
    """
    cloth_dir = dataset_dir / "cloth"
    masks_dir = dataset_dir / "masks"
    meta_dir = dataset_dir / "metadata"

    if not cloth_dir.exists():
        print(f"ERROR: No cloth directory at {cloth_dir}. Run collector.py first.")
        return

    # Create output dirs
    out_cloth = output_dir / "cloth"
    out_masks = output_dir / "agnostic-mask"
    out_image = output_dir / "image"
    out_densepose = output_dir / "image-densepose"

    for d in [out_cloth, out_masks, out_image, out_densepose]:
        d.mkdir(parents=True, exist_ok=True)

    # Copy garment images
    print(f"[Prepare] Copying garment images → {out_cloth}")
    image_extensions = {".jpg", ".jpeg", ".png", ".webp"}
    entries = []
    copied = 0
    filtered = 0

    for img_path in tqdm(list(cloth_dir.iterdir()), desc="Processing"):
        if not img_path.suffix.lower() in image_extensions:
            continue

        stem = img_path.stem

        # Check metadata for category filtering
        meta_path = meta_dir / f"{stem}.json"
        metadata = None
        category = None
        if meta_path.exists():
            with open(meta_path) as f:
                metadata = json.load(f)
            # Extract category from metadata (populated by tagger)
            if metadata and "category" in metadata:
                category = metadata["category"]
                if categories and category not in categories:
                    filtered += 1
                    continue

        # Copy image
        shutil.copy2(img_path, out_cloth / img_path.name)

        # Copy mask if exists
        mask_path = masks_dir / f"{stem}.png"
        if mask_path.exists():
            shutil.copy2(mask_path, out_masks / mask_path.name)

        # Generate caption
        caption = generate_caption(metadata, category)

        entries.append({
            "id": stem,
            "cloth": f"cloth/{img_path.name}",
            "mask": f"agnostic-mask/{stem}.png" if mask_path.exists() else None,
            "has_mask": mask_path.exists(),
            "category": category,
            "caption": caption,
            "metadata": metadata,
        })
        copied += 1

    # Filtered stats
    if filtered > 0:
        print(f"[Prepare] Filtered out {filtered} images not in: {categories}")

    # Write metadata
    metadata_out = {
        "dataset_name": "kanchuki-catvton-training",
        "prepared_at": datetime.utcnow().isoformat(),
        "total_images": len(entries),
        "categories": list(CATEGORY_LABELS.keys()),
        "image_size": "variable (will be resized during training)",
        "format": "CatVTON",
        "notes": "image/ and image-densepose/ need person images for supervised training. "
                 "For LoRA fine-tuning, only cloth/ and agnostic-mask/ are needed.",
        "entries": entries,
    }

    meta_out_path = output_dir / "metadata.json"
    with open(meta_out_path, "w") as f:
        json.dump(metadata_out, f, indent=2, default=str)

    print(f"\n[Prepare] ✅ Dataset prepared at {output_dir}")
    print(f"[Prepare]   Total images: {len(entries)}")
    print(f"[Prepare]   With masks: {sum(1 for e in entries if e['has_mask'])}")
    print(f"[Prepare]   Metadata: {meta_out_path}")


def prepare_flux_lora_dataset(
    dataset_dir: Path,
    output_dir: Path,
    categories: list[str] | None = None,
):
    """
    Prepare dataset for CatVTON-FLUX LoRA training using AI-Toolkit or Kohya.
    
    Structure:
        output/
        ├── cloth_001.png           # Garment images (sequential naming)
        ├── cloth_001.txt           # Caption for each image
        ├── cloth_001_mask.png      # Optional: segmentation mask
        ├── ...
        └── metadata.json

    Flux LoRA trains on (image, caption) pairs with optional condition images.
    """
    cloth_dir = dataset_dir / "cloth"
    masks_dir = dataset_dir / "masks"
    meta_dir = dataset_dir / "metadata"

    if not cloth_dir.exists():
        print(f"ERROR: No cloth directory at {cloth_dir}")
        return

    output_dir.mkdir(parents=True, exist_ok=True)
    captions_dir = output_dir / "captions"
    captions_dir.mkdir(exist_ok=True)

    image_extensions = {".jpg", ".jpeg", ".png", ".webp"}
    entries = []
    idx = 0

    print(f"[FLUX] Preparing LoRA dataset at {output_dir}")
    for img_path in tqdm(list(cloth_dir.iterdir()), desc="Preparing"):
        if not img_path.suffix.lower() in image_extensions:
            continue

        stem = img_path.stem

        # Load metadata
        meta_path = meta_dir / f"{stem}.json"
        metadata = None
        if meta_path.exists():
            with open(meta_path) as f:
                metadata = json.load(f)

        category = metadata.get("category") if metadata else None
        if categories and category and category not in categories:
            continue

        idx += 1
        img_id = f"kanchuki_{idx:05d}"

        # Copy image with standard name
        ext = img_path.suffix
        new_img_path = output_dir / f"{img_id}{ext}"
        shutil.copy2(img_path, new_img_path)

        # Generate caption
        caption = generate_caption(metadata, category)
        caption_path = captions_dir / f"{img_id}.txt"
        with open(caption_path, "w") as f:
            f.write(caption)

        # Copy mask
        mask_path = masks_dir / f"{stem}.png"
        new_mask_path = output_dir / f"{img_id}_mask.png"
        if mask_path.exists():
            shutil.copy2(mask_path, new_mask_path)

        entries.append({
            "id": img_id,
            "image": f"{img_id}{ext}",
            "caption_file": f"captions/{img_id}.txt",
            "caption": caption,
            "has_mask": mask_path.exists(),
            "category": category,
        })

    # Write manifest
    manifest = {
        "dataset_name": "kanchuki-ethnic-wear-lora",
        "prepared_at": datetime.utcnow().isoformat(),
        "total_images": idx,
        "format": "flux-lora",
        "categories": list(CATEGORY_LABELS.keys()),
        "entries": entries,
    }

    manifest_path = output_dir / "dataset.json"
    with open(manifest_path, "w") as f:
        json.dump(manifest, f, indent=2, default=str)

    print(f"\n[FLUX] ✅ LoRA dataset prepared at {output_dir}")
    print(f"[FLUX]   Total images: {idx}")
    print(f"[FLUX]   With masks: {sum(1 for e in entries if e['has_mask'])}")
    print(f"[FLUX]   Manifest: {manifest_path}")
    print(f"[FLUX]   Training command hint:")
    print(f"         Use AI-Toolkit with config pointing to {output_dir}/dataset.json")


# ─── CLI ───────────────────────────────────────────────────────

def main():
    parser = argparse.ArgumentParser(
        description="Package dataset for CatVTON / CatVTON-FLUX LoRA fine-tuning"
    )
    parser.add_argument(
        "--dataset-dir", "-d",
        type=str,
        default=str(Path(__file__).resolve().parent.parent.parent / "dataset"),
        help="Dataset root directory (default: project-root/dataset)",
    )
    parser.add_argument(
        "--output", "-o",
        type=str,
        default="./catvton-training-data",
        help="Output directory",
    )
    parser.add_argument(
        "--format", "-f",
        choices=["catvton", "flux-lora"],
        default="flux-lora",
        help="Output format: 'catvton' (full training) or 'flux-lora' (LoRA only, default)",
    )
    parser.add_argument(
        "--categories",
        type=str,
        nargs="+",
        help="Filter by categories (e.g. Saree Lehenga Kurti)",
    )

    args = parser.parse_args()
    dataset_dir = Path(args.dataset_dir)
    output_dir = Path(args.output)

    if args.format == "catvton":
        prepare_catvton_dataset(dataset_dir, output_dir, args.categories)
    else:
        prepare_flux_lora_dataset(dataset_dir, output_dir, args.categories)


if __name__ == "__main__":
    main()
